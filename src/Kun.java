public class Kun {
	
	/** Souradnice kone x*/
	private int x;
	/** Souradnice kone y*/
	private int y;
	/** hmotnost kone s vybavenim */
	private int m;
	/** doba nalozeni kone */
	private int n;

	/**
	 * Konstruktor kone
	 * @param x - x-ova souradnice
	 * @param y - y-ova souradnice
	 * @param m - hmotnost kone
	 * @param n - doba nalozeni
	 */
	public Kun(int x, int y, int m, int n) {
		this.x = x;
		this.y = y;
		this.m = m;
		this.n = n;
	}

}
